@pytest.mark.django_db
def test_excluded_subdirectories_are_not_recursed(
    fresh_migration_history, migrations_dir
):
    """Files under ``applied/``, ``archive/``, and ``migrations/`` are skipped.

    The command iterates only the top level of the migrations directory,
    so any ``.sql`` file placed in one of these excluded subdirectories
    must not be applied or recorded.
    """
    # Top-level file: should be applied.
    _write(
        migrations_dir / "0001_top_level.sql",
        "CREATE TABLE IF NOT EXISTS _aux_smoke_top (id INT PRIMARY KEY);",
    )

    # Files inside excluded subdirectories: must be skipped.
