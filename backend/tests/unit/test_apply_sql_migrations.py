"""Tests for the apply_sql_migrations management command.

Verifies:
- Refuses to run when the migration_history extend migration has not yet
  been applied (missing ``checksum`` column).
- Pending migrations are applied in filename order and recorded in
  ``migration_history`` with ``migration_name`` + sha256 ``checksum``.
- Already-applied migrations are skipped on re-run via
  ``ON CONFLICT (migration_name) DO NOTHING``.
- Failure in one migration raises CommandError; earlier successes kept.
- ``--dry-run`` lists pending migrations without applying them.
- Subdirectories under the migrations directory (``applied/``, ``archive/``,
  ``migrations/``) are not recursed into.

These tests cover the Component 2 refactor from
``.kiro/specs/production-schema-reconciliation/`` (task 1.2): the command
now writes to ``migration_history`` instead of the retired
``applied_sql_migrations`` table.
"""
from __future__ import annotations

import hashlib
from io import StringIO
from pathlib import Path

import pytest
from django.core.management import call_command
from django.core.management.base import CommandError
from django.db import connection


# DDL used by the fixtures to build a ``migration_history`` table that
# matches the post-extend shape (``checksum`` and ``notes`` columns +
# unique index on ``migration_name``). On Postgres this mirrors what
# ``2026_05_22_migration_history_extend.sql`` produces; on SQLite it
# uses equivalent column types so the same test suite runs under both
# vendors.

_PG_EXTENDED_DDL = """
CREATE TABLE migration_history (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    migration_name TEXT NOT NULL,
    applied_at TIMESTAMPTZ NULL,
    checksum TEXT NULL,
    notes TEXT NULL
);
CREATE UNIQUE INDEX uq_migration_history_migration_name
    ON migration_history (migration_name);
"""

_SQLITE_EXTENDED_DDL = """
CREATE TABLE migration_history (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    migration_name TEXT NOT NULL,
    applied_at TEXT NULL,
    checksum TEXT NULL,
    notes TEXT NULL
);
CREATE UNIQUE INDEX uq_migration_history_migration_name
    ON migration_history (migration_name);
"""

_PG_LEGACY_DDL = """
CREATE TABLE migration_history (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    migration_name TEXT NOT NULL,
    applied_at TIMESTAMPTZ NULL
);
"""

_SQLITE_LEGACY_DDL = """
CREATE TABLE migration_history (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    migration_name TEXT NOT NULL,
    applied_at TEXT NULL
);
"""


def _drop_migration_history() -> None:
    with connection.cursor() as cur:
        cur.execute("DROP INDEX IF EXISTS uq_migration_history_migration_name")
        cur.execute("DROP TABLE IF EXISTS migration_history")


def _create_extended_migration_history() -> None:
    ddl = _SQLITE_EXTENDED_DDL if connection.vendor == "sqlite" else _PG_EXTENDED_DDL
    with connection.cursor() as cur:
        for statement in [s.strip() for s in ddl.split(";") if s.strip()]:
            cur.execute(statement)


def _create_legacy_migration_history() -> None:
    """Build the pre-extend ``migration_history`` shape (no ``checksum``)."""
    ddl = _SQLITE_LEGACY_DDL if connection.vendor == "sqlite" else _PG_LEGACY_DDL
    with connection.cursor() as cur:
        for statement in [s.strip() for s in ddl.split(";") if s.strip()]:
            cur.execute(statement)


