@pytest.mark.django_db
class TestApplySqlMigrationsLint:
    """The command rejects non-additive files unless the bypass flag is set."""

    def test_drop_column_file_is_rejected_with_canonical_message(
        self, fresh_migration_history, migrations_dir
    ):
        _write(
            migrations_dir / "0001_drop_column.sql",
            "ALTER TABLE foo DROP COLUMN bar;",
        )

        out, err = StringIO(), StringIO()
        with pytest.raises(CommandError) as exc:
            call_command(
                "apply_sql_migrations",
                "--migrations-dir",
                str(migrations_dir),
                stdout=out,
                stderr=err,
            )

        assert (
            "REJECTED_NON_ADDITIVE_OPERATION: DROP COLUMN in 0001_drop_column.sql"
            in err.getvalue()
        )
        # CommandError carries the same offending pattern for log surfaces.
        assert "DROP COLUMN" in str(exc.value)

        # No row should have been written into ``migration_history``
        # because the file was rejected pre-execution.
        with connection.cursor() as cur:
            cur.execute("SELECT COUNT(*) FROM migration_history")
            assert cur.fetchone()[0] == 0

    def test_drop_table_file_is_rejected(
        self, fresh_migration_history, migrations_dir
    ):
        _write(migrations_dir / "0001_drop_table.sql", "DROP TABLE foo;")

        err = StringIO()
        with pytest.raises(CommandError):
            call_command(
                "apply_sql_migrations",
                "--migrations-dir",
                str(migrations_dir),
                stdout=StringIO(),
                stderr=err,
            )

        assert (
            "REJECTED_NON_ADDITIVE_OPERATION: DROP TABLE in 0001_drop_table.sql"
            in err.getvalue()
        )

    def test_truncate_file_is_rejected(
        self, fresh_migration_history, migrations_dir
    ):
        _write(migrations_dir / "0001_truncate.sql", "TRUNCATE TABLE foo;")

        err = StringIO()
        with pytest.raises(CommandError):
            call_command(
                "apply_sql_migrations",
                "--migrations-dir",
                str(migrations_dir),
                stdout=StringIO(),
                stderr=err,
            )

        assert (
            "REJECTED_NON_ADDITIVE_OPERATION: TRUNCATE in 0001_truncate.sql"
            in err.getvalue()
        )

    def test_unbounded_delete_file_is_rejected(
        self, fresh_migration_history, migrations_dir
    ):
        _write(migrations_dir / "0001_delete.sql", "DELETE FROM foo;")

        err = StringIO()
        with pytest.raises(CommandError):
            call_command(
                "apply_sql_migrations",
                "--migrations-dir",
                str(migrations_dir),
                stdout=StringIO(),
                stderr=err,
            )

        assert (
            "REJECTED_NON_ADDITIVE_OPERATION: DELETE FROM (without WHERE) "
            "in 0001_delete.sql"
            in err.getvalue()
        )

    def test_bounded_delete_file_is_allowed(
        self, fresh_migration_history, migrations_dir
    ):
        """A ``DELETE ... WHERE`` is a permitted targeted cleanup
        (used by, e.g., the migration_history reconcile rollback).
        """
        # First create the table the DELETE will target so the SQL
        # actually executes cleanly on SQLite.
        with connection.cursor() as cur:
            cur.execute(
                "CREATE TABLE IF NOT EXISTS _aux_lint_delete "
                "(id INT PRIMARY KEY, name TEXT)"
            )
            cur.execute("INSERT INTO _aux_lint_delete VALUES (1, 'a')")

        _write(
            migrations_dir / "0001_bounded_delete.sql",
            "DELETE FROM _aux_lint_delete WHERE id = 999;",
        )

        call_command(
            "apply_sql_migrations",
            "--migrations-dir",
            str(migrations_dir),
            stdout=StringIO(),
        )

        with connection.cursor() as cur:
            cur.execute(
                "SELECT migration_name FROM migration_history "
                "WHERE migration_name = %s",
                ["0001_bounded_delete.sql"],
            )
            assert cur.fetchone() is not None
            cur.execute("DROP TABLE IF EXISTS _aux_lint_delete")

    def test_alter_column_type_using_file_is_rejected(
        self, fresh_migration_history, migrations_dir
    ):
        _write(
            migrations_dir / "0001_alter_using.sql",
            "ALTER TABLE foo ALTER COLUMN bar TYPE INTEGER USING bar::integer;",
        )

        err = StringIO()
        with pytest.raises(CommandError):
            call_command(
                "apply_sql_migrations",
                "--migrations-dir",
                str(migrations_dir),
                stdout=StringIO(),
                stderr=err,
            )

        assert (
            "REJECTED_NON_ADDITIVE_OPERATION: ALTER COLUMN TYPE USING "
            "in 0001_alter_using.sql"
            in err.getvalue()
        )

    def test_allow_non_additive_flag_bypasses_lint(
        self, fresh_migration_history, migrations_dir
    ):
        """``--allow-non-additive`` lets a DROP-flavoured file apply."""
        # Create a target table the DROP will operate against so the
        # SQL itself is valid, otherwise the test would fail for a
        # different reason than the lint.
        with connection.cursor() as cur:
            cur.execute(
                "CREATE TABLE IF NOT EXISTS _aux_lint_bypass "
                "(id INT PRIMARY KEY, doomed TEXT)"
            )

        if connection.vendor == "sqlite":
            # SQLite supports ``ALTER TABLE ... DROP COLUMN`` only
            # since 3.35; the syntax is otherwise the same. Use a
            # ``DROP TABLE`` payload which works on every supported
            # vendor.
            sql = "DROP TABLE _aux_lint_bypass;"
        else:
            sql = "ALTER TABLE _aux_lint_bypass DROP COLUMN doomed;"

        _write(migrations_dir / "0001_bypass.sql", sql)

        out = StringIO()
        call_command(
            "apply_sql_migrations",
            "--migrations-dir",
            str(migrations_dir),
            "--allow-non-additive",
            stdout=out,
        )

        with connection.cursor() as cur:
            cur.execute(
                "SELECT migration_name FROM migration_history "
                "WHERE migration_name = %s",
                ["0001_bypass.sql"],
            )
            assert cur.fetchone() is not None
            cur.execute("DROP TABLE IF EXISTS _aux_lint_bypass")

    def test_dry_run_still_rejects_non_additive_file(
        self, fresh_migration_history, migrations_dir
    ):
        """Operators planning a release see the rejection during
        ``--dry-run`` so they can re-author the file before the
        maintenance window.
        """
        _write(
            migrations_dir / "0001_drop_column_dry.sql",
            "ALTER TABLE foo DROP COLUMN bar;",
        )

        err = StringIO()
        with pytest.raises(CommandError):
            call_command(
                "apply_sql_migrations",
                "--migrations-dir",
                str(migrations_dir),
                "--dry-run",
                stdout=StringIO(),
                stderr=err,
            )

        assert (
            "REJECTED_NON_ADDITIVE_OPERATION: DROP COLUMN "
            "in 0001_drop_column_dry.sql"
            in err.getvalue()
        )

    def test_clean_additive_file_passes(
        self, fresh_migration_history, migrations_dir
    ):
        """A baseline regression: a fully additive file must apply
        cleanly with the lint enabled.
        """
        _write(
            migrations_dir / "0001_clean.sql",
            "CREATE TABLE IF NOT EXISTS _aux_lint_clean (id INT PRIMARY KEY);",
        )

        call_command(
            "apply_sql_migrations",
            "--migrations-dir",
            str(migrations_dir),
            stdout=StringIO(),
        )

        with connection.cursor() as cur:
            cur.execute(
                "SELECT migration_name FROM migration_history "
                "WHERE migration_name = %s",
                ["0001_clean.sql"],
            )
            assert cur.fetchone() is not None
            cur.execute("DROP TABLE IF EXISTS _aux_lint_clean")

    def test_drop_column_in_comment_does_not_trigger_lint(
        self, fresh_migration_history, migrations_dir
    ):
        """A documentation comment mentioning a future drop must not
        be confused for an actual non-additive operation.
        """
        _write(
            migrations_dir / "0001_comment_only.sql",
            "-- DROP COLUMN bar (planned 2026-08-15)\n"
            "/* TRUNCATE planned for legacy table */\n"
            "CREATE TABLE IF NOT EXISTS _aux_lint_comment "
            "(id INT PRIMARY KEY);\n",
        )

        call_command(
            "apply_sql_migrations",
            "--migrations-dir",
            str(migrations_dir),
            stdout=StringIO(),
        )

        with connection.cursor() as cur:
            cur.execute(
                "SELECT migration_name FROM migration_history "
                "WHERE migration_name = %s",
                ["0001_comment_only.sql"],
            )
            assert cur.fetchone() is not None
            cur.execute("DROP TABLE IF EXISTS _aux_lint_comment")
